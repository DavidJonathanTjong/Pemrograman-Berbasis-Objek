    private void inisialisasiTabel() {
        DefaultTableModel tmodel = (DefaultTableModel) tabelSuplier.getModel();
        tabelSuplier.setAutoCreateRowSorter(true);
        try {
            String query = "SELECT * FROM suplier";
            PreparedStatement preparedStatement = con.prepareStatement(query);
            ResultSet resultSet = preparedStatement.executeQuery();
            while (resultSet.next()) {
                int idSupplier = resultSet.getInt("idSuplier");
                String namaSupplier = resultSet.getString("nama");
                String alamat = resultSet.getString("alamat");
                String kota = resultSet.getString("kota");
                String provinsi = resultSet.getString("provinsi");
                String nomorTelpon = resultSet.getString("nomortelpon");
                ArrayList mySuplier = new ArrayList();
                mySuplier.add(idSupplier);
                mySuplier.add(namaSupplier);
                mySuplier.add(alamat);
                mySuplier.add(kota);
                mySuplier.add(provinsi);
                mySuplier.add(nomorTelpon);
                tmodel.addRow(mySuplier.toArray());
            }
            preparedStatement.close();
            resultSet.close();
        } catch (SQLException ex) {
            System.out.println(ex + "gagal inisialisasi tabel suplier");
        }
    }

