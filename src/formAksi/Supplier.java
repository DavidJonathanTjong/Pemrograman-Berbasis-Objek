    private void butTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butTambahActionPerformed
        if (inp1.getText().isEmpty() || inp3.getText().isEmpty() || inp4.getText().isEmpty() || inp5.getText().isEmpty()
                || inp6.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Tidak dapat menambahkan, isi yang kosong", "Notif", JOptionPane.INFORMATION_MESSAGE);
        } else {
            try {
                int idSupplier = Integer.parseInt(inp1.getText());
                String namaSupplier = inp2.getText();
                String alamat = inp3.getText();
                String kota = inp4.getText();
                String provinsi = inp5.getText();
                String nomorTelpon = inp6.getText();

                if (cekIDSuplier(idSupplier)) {
                    JOptionPane.showMessageDialog(null, "ID Suplier sudah ada", "Notif", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    String query = "INSERT INTO suplier (idSuplier, nama, alamat, kota, provinsi, nomortelpon) VALUES (?, ?, ?, ?, ?, ?)";
                    PreparedStatement preparedStatement = con.prepareStatement(query);
                    preparedStatement.setInt(1, idSupplier);
                    preparedStatement.setString(2, namaSupplier);
                    preparedStatement.setString(3, alamat);
                    preparedStatement.setString(4, kota);
                    preparedStatement.setString(5, provinsi);
                    preparedStatement.setString(6, nomorTelpon);

                    int hasilEksekusi = preparedStatement.executeUpdate();
                    if (hasilEksekusi > 0) {
                        JOptionPane.showMessageDialog(null, "Data berhasil ditambahkan ke dalam tabel supplier", "Notif", JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        JOptionPane.showMessageDialog(null, "Gagal menambahkan data ke dalam tabel supplier", "Notif", JOptionPane.INFORMATION_MESSAGE);
                    }
                    preparedStatement.close();
                }
            } catch (Exception e) {
                System.out.println("gagal tambah item");
            }
            DefaultTableModel tmodel = (DefaultTableModel) tabelSuplier.getModel();
            tmodel.setRowCount(0);
            inisialisasiTabel();
        }
    }//GEN-LAST:event_butTambahActionPerformed

    private void butDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butDelActionPerformed
        int row = tabelSuplier.getSelectedRow();
        int value = Integer.parseInt(tabelSuplier.getModel().getValueAt(row, 0).toString());
        try {
            String query = "DELETE FROM suplier WHERE idSuplier = ?";
            PreparedStatement preparedStatement = con.prepareStatement(query);
            preparedStatement.setInt(1, value);
            int rowDeleted = preparedStatement.executeUpdate();
            if (rowDeleted > 0) {
                System.out.println("Data berhasil dihapus dari tabel supplier");
            } else {
                System.out.println("Gagal menghapus data dari tabel supplier");
            }
            preparedStatement.close();
        } catch (SQLException ex) {
            System.out.println("Gagal delete baris yang dipilih pada tabel supplier");
        }
        DefaultTableModel tmodel = (DefaultTableModel) tabelSuplier.getModel();
        tmodel.setRowCount(0);
        inisialisasiTabel();
    }//GEN-LAST:event_butDelActionPerformed

    private boolean cekIDSuplier(int value) {
        try {
            String query = "SELECT COUNT(*) FROM suplier WHERE idSuplier = ?";
            PreparedStatement preparedStatement = con.prepareStatement(query);
            preparedStatement.setInt(1, value);
            ResultSet resultSet = preparedStatement.executeQuery();
            resultSet.next();
            int count = resultSet.getInt(1);
            resultSet.close();
            preparedStatement.close();
            return count > 0;
        } catch (SQLException ex) {
            Logger.getLogger(Supplier.class.getName()).log(Level.SEVERE, null, ex);
        }
        return false;
    }

